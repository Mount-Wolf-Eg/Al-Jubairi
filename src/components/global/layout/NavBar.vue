  if (lan == "ar") {
    lang.value = "AR";
    sessionStorage.setItem("lang", "ar");
    document.querySelector("html").setAttribute("dir", "rtl");
    window.location.reload();
  } else {
    lang.value = "EN";
    sessionStorage.setItem("lang", "en");
    document.querySelector("html").setAttribute("dir", "ltr");
    window.location.reload();
  }
};
